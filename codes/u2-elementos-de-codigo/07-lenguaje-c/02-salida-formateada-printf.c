#include <stdio.h>

int main()
{
    int nota1 = 10;
    int nota2 = 7;
    float dec = 34.9814;
    char letra = 'e';

    printf("Salida de enteros: nota1: %d y nota2: %d \n", nota1, nota2);
    printf("Salida de flotantes: (completo) %f \t (formato con 2 decimales) %.2f \n", dec, dec);
    printf("Salida de caracter: %c \n", letra);
}

